function LegalBlocks({ items }) {
  // turn a bare email into an orange underlined mailto link
  const renderText = (t) => {
    const m = t.match(/([\w.+-]+@[\w.-]+\.[a-z]{2,})/i);
    if (!m) return t;
    const [before, after] = t.split(m[0]);
    return <React.Fragment>{before}<a className="sf-legal-mail" href={'mailto:' + m[0]}>{m[0]}</a>{after}</React.Fragment>;
  };
  return items.map((b, i) => {
    if (b.p) return <p className="sf-legal-p" key={i}>{renderText(b.p)}</p>;
    if (b.label) return <p className="sf-legal-label" key={i}>{b.label}</p>;
    if (b.ul) return <ul className="sf-legal-ul" key={i}>{b.ul.map((x, j) => <li key={j}>{x}</li>)}</ul>;
    if (b.ol) return <ol className="sf-legal-ol" key={i}>{b.ol.map((x, j) => <li key={j}>{x}</li>)}</ol>;
    return null;
  });
}

